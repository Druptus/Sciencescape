# Variables are like labeled boxes - they store a value under a specified name.
# There are many different types of variables.
# Python will automatically figure out what type of variable you are currently using based on the value you assign to it.

# For example, here are some different variable types with their values:
river = "Wabash River" # This is a string (it stores text)
ph = 7.2 # This is a float (it stores a decimal number like a fraction)
temp_c = 18.5 # This is another example of a float
is_safe = True # This is a boolean (it stores either True or False)
bottles_recycled = 100 # This is an integer (it stores a whole number)

# To output a variable, you can use the print() function and pass in the variable name.
# For example, here are the variables we defined above being printed to the screen:
print("River:", river)
print("pH:", ph)
print("Temperature (C):", temp_c)
print("Safe to swim?", is_safe)

